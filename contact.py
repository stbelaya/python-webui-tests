class Contact:
    def __init__(self, firstname, middlename, lastname, nickname, title, address, mobile, email, bday, bmonth,
                       byear):
        self.firstname = firstname
        self.middlename = middlename
        self.lastname = lastname
        self.nickname = nickname
        self.title = title
        self.address = address
        self.mobile = mobile
        self.email = email
        self.bday = bday
        self.bmonth = bmonth
        self.byear = byear
